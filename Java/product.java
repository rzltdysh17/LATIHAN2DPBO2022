// Kelas Product
class Product
{
    // Atribut Private
    private int price;
    private String idProduct;

    // Constructor
    Product()
    {
        
    }

    // Prosedur untuk Setter dan Getter
    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getIdProduct() {
        return idProduct;
    }

    public void setIdProduct(String idProduct) {
        this.idProduct = idProduct;
    }

}
